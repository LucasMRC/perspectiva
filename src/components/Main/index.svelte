            A veces me descubro sintiendo que <em>le encuentro la lógica a la vida</em>. Que puedo darle forma a los sucesos que acontecen (por lo menos, una forma medio intelectual). Que si veo las cosas de una u otra perspectiva, entonces resignifico la experiencia y la modifico. Claro, el truco es sólo <strong>elegir la perspectiva</strong>.
        </p>
        <p>
            También me pasa que a medida que pasa el tiempo, cada vez son menos las certezas que tengo. Así, mis acciones se encuentran escoltadas por preguntas, de esas que <strong>no puedo responder sin al menos ser imparcial</strong>.
        </p>
        <p>
            Muy pocas son las cosas que hago y que sé realmente <strong><em>si estoy actuando de manera correcta o inteligente</em></strong>. Para el resto de mi vida, no sé si estoy siendo paciente o me estoy dejando estar. Si aguantar un poco el dolor me ayuda a construir mi carácter, o es mejor tomar un analgésico para poder descansar. Si debo ayudar al otro, o en enfocarme en mis cosas y trabajar en pos de mis objetivos.
        </p>
        <p>
            <em>No tengo métricas para medir mis decisiones</em>. Muchas de las cosas que hago son correctas o no <strong>dependiendo del lugar desde el que se las mire</strong>. Y no soy yo quien debe elegir ese lugar, a mí me toca actuar.
        </p>
        <p>
            Así que acá estoy, a mis 32 años, estableciendo acuerdos conmigo mismo, <strong>normas de convivencia</strong>. Principalmente, una <em>norma de doble-confianza</em>.
        </p>
        <p>
            Primero, confianza en mí, Lucas. En que las decisiones de antes me trajeron a este lugar. <em>Que no me ha salido tan mal</em>, después de todo. Y que con el tiempo, he ido buscando desarrollar un criterio para tomar decisiones. Bueno o malo, <strong>pero propio</strong>. Messirve.
        </p>
        <p>
            Segundo, <em>confianza en la otra parte</em>. A estas alturas ya he aprendido que las cosas toman sentido con el tiempo, la historia les va dando significado. Difícilmente pueda yo ver hoy cómo se van a desenvolver los acontecimientos. En la mayoría de los casos, no me queda más que <strong>tomar una decisión</strong>. Jugármela, y después confiar.
        </p>
        <p>
            Quizás esa sea mi vara vaya por aquel lado. En toda esta incertidumbre, si sigue todo teniendo sentido. ¿Con cuánta flexibilidad y elegancia puedo seguir dándole forma a mi experiencia de estar vivo?
        </p>
        <p>
            De esa forma, permitiré que <strong>la perspectiva</strong> sea la métrica por la que mido mi vida.
        </p>
