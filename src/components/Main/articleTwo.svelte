    <p>
        Las cosas que damos por sentado son las cosas que más extrañamos cuando se van. Y a veces, con la culpa de no haberles dado demasiada importancia mientras estaban. Cosas como <strong>caminar, andar en bici o correr</strong>, ¿cuántas veces me detuve a agradecer por eso?
    </p>
    <p>
        Quizás porque nunca penśe que me faltarían alguna vez. Porque <em>no me la vi venir</em>. Y esa falta de despedida es una de mis sensaciones menos favoritas. Es difícil el desapego, <strong>sobre todo el inesperado</strong>. Pero afortunadamente, no es mi primera vez.
    </p>
    <p>
        Me encantaría desarrollar un <strong>sentido de responsabilidad</strong> hacia estas cosas obvias de todos los días. Para prevenir la falta de <em>celebración de las pequeñas cosas</em> (y no tan pequeñas). Y para eso me gustaría hacer un recuento.
    </p>
    <p>
        Y no puedo empezar sin sincerarme: doy por sentado mis manos. Mis herramientas más grandes, mi instrumento de trabajo. La música, la escritura, las caricias.
    </p>
</article>

